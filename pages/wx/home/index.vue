      try {
        const src = nextBgm()
        bgmInstance.src = src
        if (isBgmPlaying.value) {
          bgmInstance.play().catch(() => {
            isBgmPlaying.value = false
            uni.showToast({ title: '背景音乐需手动播放', icon: 'none' })
          })
        }
      } catch (e) {
        // 忽略，保持静音状态
      }
    }
  }
  return bgmInstance
}

